    private void loginakunActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_loginakunActionPerformed
        String uname = id.getText();
        String pass = pw.getText();
        String option = jablog2.getSelectedItem().toString();

        if(option.equals("Select")||uname.equals("")||pass.equals("")){
            JOptionPane.showMessageDialog(rootPane,"Ada yang belum Terisi","Error", 1);
        }else{
            try{
                akses = koneksi.akses();
                pre = akses.prepareStatement("select * from tb_user where username=? and password=?");
                pre.setString(1, uname);
                pre.setString(2, pass);
                rs = pre.executeQuery();
                if(rs.next()){
                    String s1 = rs.getString("role");
                    String un = rs.getString("username");
                    if(option.equalsIgnoreCase("admin")&& s1.equalsIgnoreCase("admin")){
                        Hak.setText(un);
                        Paket.setVisible(true);
                        Register.setVisible(true);
                        tabtra.setVisible(false);
                        transaksi.setVisible(false);
                        login.setVisible(false);
                        kerja.setVisible(true);
                        traedit.setVisible(true);
                        trahapus.setVisible(true);
                        pakethapus.setVisible(true);
                        editpaket.setVisible(true);
                        editstruk.setVisible(true);
                        editorang1.setVisible(true);
                        memhapus.setVisible(true);
                        hapustruk.setVisible(true);
                        exit.setVisible(true);
                        Member.setVisible(true);
                        Paket.setVisible(true);
                        Transaksi.setVisible(true);
                        Register.setVisible(true);
                        Lapor.setVisible(true);
                    }
                    else if (option.equalsIgnoreCase("owner")&& s1.equalsIgnoreCase("owner")){
                        Hak.setText(un);
                        editstruk.setVisible(false);
                        hapustruk.setVisible(false);
                        laporgan.setVisible(true);
                        exit.setEnabled(true);
                        login.setVisible(false);
                        transaksi.setVisible(false);
                        tabtra.setVisible(false);
                        paket.setVisible(false);
                        register.setVisible(false);
                        id.setText("");
                        pw.setText("");
                        jablog2.setSelectedItem("Pilih");
                    }
                    else if(option.equalsIgnoreCase("kasir")&& s1.equalsIgnoreCase("kasir")){
                        Hak.setText(un);
                        Paket.setVisible(false);
                        Register.setVisible(false);
                        traedit.setVisible(false);
                        trahapus.setVisible(false);
                        pakethapus.setVisible(false);
                        editpaket.setVisible(false);
                        editstruk.setVisible(false);
                        editorang1.setVisible(false);
                        memhapus.setVisible(false);
                        hapustruk.setVisible(false);
                        kerja.setVisible(true);
                        akun.setVisible(false);
                        laporgan.setVisible(false);
                        Member.setVisible(true);
                        login.setVisible(false);
                        transaksi.setVisible(false);
                        exit.setVisible(true);
                        Transaksi.setVisible(true);
                        Lapor.setVisible(true);
                        id.setText("");
                        pw.setText("");
                        jablog2.setSelectedItem("Pilih");
                    
                    }
                }else{
                    JOptionPane.showMessageDialog(rootPane,"Username atau Password anda tidak di temukan atau salah","Log in Error", 1);
                }
            }catch(HeadlessException | ClassNotFoundException | SQLException ex){
                JOptionPane.showMessageDialog(rootPane,"Ada yang belum diisi","Log in Error", 1);
            }
        }
    }//GEN-LAST:event_loginakunActionPerformed

